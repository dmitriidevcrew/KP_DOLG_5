using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace KP_DOLG_5
{
    public class MyClass
    {

        public int MySum(int a, int b, int c)
        {
            return a + b + c;
        }

        public int MyPow(int a)
        {
            return a * a * a;
        }

        public int MyMax(int a, int b)
        {
            return a > b ? a : b;
        }
    }
}
